class Partner {
  String _img;
  String _correo;
  String _telefono;
  String _wa;

  get img => _img;
  set img(String value) {
    _img = value;
  }

  get correo => _correo;
  set correo(String value) {
    _correo = value;
  }

  get telefono => _telefono;
  set telefono(String value) {
    _telefono = value;
  }

  get wa => _wa;
  set wa(String value) {
    _wa = value;
  }

  Partner();
}